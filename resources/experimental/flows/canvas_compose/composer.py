"""CanvasComposer -- canvas-first poster painting for TMNF stadium-car skins.

This module hosts the central class :class:`CanvasComposer` which orchestrates
the new (canvas-first) skin pipeline.  Compared to the previous per-UV-island
pipeline, the rules of engagement are:

* The Diffuse is composed on a flat 2048x2048 RGBA *canvas*.  You paint
  motifs across the entire surface; UV islands are not used as masks during
  composition, only as a final blackout (so hidden polygons stay black).
* The Details (4096x4096) inherits the template's mechanical detail except
  for a small set of canonical "paintable" regions (wheel rim, suit, visor)
  which are recolored with the theme's accent colors.
* The whole skin shares a single alpha value chosen from
  :class:`MaterialFinish` (MATTE / DEFAULT / WET / GLOSS / CHROME).
* All known wordmark / branding regions in the template (CH "CAVERN
  HUNTERS" logo, KACKY label, WHEELHAND debug text, etc.) are blacked
  out so that we never inherit any copyrighted text.

The composer is intentionally small (<400 lines) and contains *no*
preset-specific logic; presets live in :mod:`skins.canvas_compose.motifs`
and are passed in as Motif objects.

Usage:

    from skins.canvas_compose import CanvasComposer, MaterialFinish
    from skins.canvas_compose.motifs import Gradient, BrushStreaks

    cc = CanvasComposer(theme_name="Aurora", seed=42)
    cc.paint(Gradient(stops=[...]))
    cc.paint(BrushStreaks(palette=[...]))
    cc.recolor_details({"rim_inner": (60, 200, 255), "suit_chest": (200,255,255)})
    cc.set_finish(MaterialFinish.DEFAULT)
    out_zip = cc.save(Path("out/Aurora.zip"))
"""
