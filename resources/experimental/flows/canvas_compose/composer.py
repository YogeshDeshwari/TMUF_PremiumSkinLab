    # Internal helpers
    # ------------------------------------------------------------------

    @staticmethod
    def _fill_mask_from_surround(arr: np.ndarray, mask: np.ndarray) -> None:
        """Replace `arr` pixels under `mask` with the mean of nearby non-mask pixels.

        Implementation: weighted-blur inpaint.  Zero out the masked signal,
        blur it, blur the keep-mask, divide -- each masked pixel ends up
        with the local average of valid pixels around it.  Uses
        :func:`scipy.ndimage.gaussian_filter` (this version of PIL refuses
        to blur "F" mode images on macOS).
        """
        from scipy.ndimage import gaussian_filter

        rgb = arr[..., :3].astype(np.float32)
        keep = (~mask).astype(np.float32)
        sigma = 6.0

        blur_sig = np.stack([
            gaussian_filter(rgb[..., c] * keep, sigma=sigma, mode="reflect")
            for c in range(3)
        ], axis=-1)
        blur_w = gaussian_filter(keep, sigma=sigma, mode="reflect")
        blur_w = np.clip(blur_w, 1e-3, None)
        avg = blur_sig / blur_w[..., None]

        rgb[mask] = avg[mask]
        arr[..., :3] = np.clip(rgb, 0, 255).astype(np.uint8)
        arr[..., 3] = 255

